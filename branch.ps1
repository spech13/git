# git branch <branch_name> - create new branch with name "branch_name"
git branch BRN-1212

# git branch -a - output all branches
git branch -a

# git branch -m <current_branch_name> <new_branch_name> - rename branch
# from "current_branch_name" to "new_branch_name"
git branch -m BRN-1212 BRN-1313

# git branch --show-current - show current branch name
git branch --show-current